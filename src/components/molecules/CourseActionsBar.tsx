const CourseActionsBar = () => {
  const [openPopup, setOpenPopup] = useState<"question" | "leaderboard" | null>(
    null,
  );

  const scrollToSection = (sectionId: string) => {
    const el = document.getElementById(sectionId);
    if (el) {
      el.scrollIntoView({ behavior: "smooth", block: "start" });
    }
  };

  const handleActionClick = (key: ActionKey) => {
    switch (key) {
      case "curriculum":
        scrollToSection("curriculum");
        break;
      case "comments":
        scrollToSection("comments");
        break;
      case "question":
        setOpenPopup("question");
        break;
      case "leaderboard":
        setOpenPopup("leaderboard");
        break;
    }
  };

  return (
    <>
      <div className="flex items-center gap-3">
        {ACTIONS.map((action) => (
          <button
            key={action.key}
            onClick={() => handleActionClick(action.key)}
            aria-label={action.label}
            title={action.label}
            className="h-10 w-10 rounded-full border border-gray-300 text-gray-500 flex items-center justify-center cursor-pointer hover:bg-gray-50 hover:text-gray-700 transition-colors"
          >
            {action.icon}
          </button>
        ))}
      </div>

      <AskQuestionPopup
        open={openPopup === "question"}
        onClose={() => setOpenPopup(null)}
      />

      <LeaderboardPopup
        open={openPopup === "leaderboard"}
        onClose={() => setOpenPopup(null)}
      />
    </>
  );
};

export default CourseActionsBar;
